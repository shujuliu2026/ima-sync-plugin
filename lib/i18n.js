/** @param {UiLang} [pref] */
function detectLang (pref) {
  if (pref === 'zh' || pref === 'en') return pref
  const nav = (typeof navigator !== 'undefined' && navigator.language) || 'en'
  return nav.toLowerCase().startsWith('zh') ? 'zh' : 'en'
}

/** @param {{ language?: UiLang }} settings */
function resolveLang (settings) {
  return detectLang(settings?.language || 'auto')
}

/**
 * @param {{ language?: UiLang }} settings
 * @param {keyof typeof STR.zh} key
 * @param {Record<string, string|number>} [vars]
 */
function t (settings, key, vars) {
  const lang = resolveLang(settings)
  let s = STR[lang][key]
  if (s === undefined) s = STR.en[key]
  if (s === undefined) s = key
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      s = s.replace(`{${k}}`, String(v))
    }
  }
  return s
}

/**
 * 设置项双语标题：auto 模式显示「英 · 中」
 * @param {{ language?: UiLang }} settings
 * @param {keyof typeof STR.zh} key
 */
function label (settings, key) {
  const lang = settings?.language
  if (lang === 'zh') return STR.zh[key]
  if (lang === 'en') return STR.en[key]
  const zh = STR.zh[key]
  const en = STR.en[key]
  if (!zh || !en || zh === en) return zh || en
  return `${en} · ${zh}`
}

function labelExp (settings, key) {
  return `${label(settings, key)} · ${t(settings, 'expTag')}`
}

/** @param {{ language?: UiLang }} settings @param {unknown} err */
function formatSyncError (settings, err) {
  const { parseImaError } = require('./ima-errors')
  const limit = parseImaError(err)
  if (limit) {
    const key = limit.kind === 'quota' ? 'quotaExceeded' : 'rateLimitExceeded'
    return `${t(settings, 'syncFailed')}: ${t(settings, key)}`
  }
  const msg = String(err?.message || err || '')
  const plain = msg.replace(/^IMA_[A-Z_]+:\s*/, '')
  if (/invalid knowledge_base_id|invalid kb_id|knowledge_base_id.*invalid/i.test(plain)) {
    return `${t(settings, 'syncFailed')}: ${t(settings, 'kbIdInvalid')}`
  }
  return `${t(settings, 'syncFailed')}: ${plain || msg}`
}

