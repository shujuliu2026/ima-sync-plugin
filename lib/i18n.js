/**
 * @param {HTMLElement} containerEl
 * @param {{ language?: UiLang }} settings
 * @param {{ onAuthorClick?: () => void }} [opts]
 */
function renderPanelAuthor (containerEl, settings, opts = {}) {
  const line = containerEl.createDiv({ cls: 'ima-author-line' })
  const prefix = t(settings, 'authorPrefix')
  const suffix = t(settings, 'authorSuffix')
  if (prefix) line.createSpan({ text: prefix })
  const nameEl = line.createSpan({ cls: 'ima-author-name', text: t(settings, 'authorName') })
  if (opts.onAuthorClick) {
    nameEl.setAttr('role', 'button')
    nameEl.setAttr('tabindex', '0')
    nameEl.addEventListener('click', opts.onAuthorClick)
    nameEl.addEventListener('keydown', (e) => {
      if (e.key === 'Enter' || e.key === ' ') {
        e.preventDefault()
        opts.onAuthorClick()
      }
    })
  }
  const followHint = t(settings, 'authorFollowHint')
  if (followHint) {
    line.createSpan({ cls: 'ima-author-follow-hint', text: followHint })
  }
  if (suffix) line.createSpan({ text: suffix })
  const site = line.createEl('a', { text: brandSiteHost, cls: 'ima-author-site' })
  site.href = brandSiteUrl
  site.target = '_blank'
  site.rel = 'noopener'
}

